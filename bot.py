def normalize_name(raw_name: str) -> str:
    cleaned = raw_name.strip().lower().replace("ё", "е")
    cleaned = " ".join(cleaned.split())
    first_word = cleaned.split()[0] if cleaned else ""
    return first_word


def canonical_name(raw_name: str) -> str:
    key = normalize_name(raw_name)
    if key in NAME_DATABASE:
        return key

    # special cases for ambiguous colloquial forms
    if key == "саша":
        return "александр"
    if key == "кира":
        # чаще женское, но у нас нет отдельной базы — оставляем как имя само по себе через fallback
        return key
    if key in NAME_ALIASES:
        return NAME_ALIASES[key]

    return key


def build_local_name_story(name: str) -> str | None:
    canonical = canonical_name(name)
    info = NAME_DATABASE.get(canonical)
    if not info:
        return None

    display_name = name.strip()
    return (
        f"{display_name} — {info['gender']} имя. {info['origin']} "
        f"{info['character']} {info['archetype']} {info['ending']}"
    )


def default_state() -> dict:
    return {
        "chat_id": None,
        "send_time": DEFAULT_SEND_TIME,
        "user_tz": USER_TZ,
        "profile": DEFAULT_PROFILE.copy(),
    }


def load_state() -> dict:
    if not STATE_FILE.exists():
        return default_state()

    try:
        state = json.loads(STATE_FILE.read_text(encoding="utf-8"))
    except Exception:
        logger.exception("Failed to read state.json, using defaults")
        return default_state()

    merged = default_state()
    merged.update({k: v for k, v in state.items() if k != "profile"})

    profile = DEFAULT_PROFILE.copy()
    profile.update(state.get("profile", {}))
    merged["profile"] = profile
    return merged


def save_state(state: dict) -> None:
    STATE_FILE.write_text(
        json.dumps(state, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )



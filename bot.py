async def animate_waiting_message(
    context: ContextTypes.DEFAULT_TYPE,
    chat_id: int,
    message_id: int,
    name: str,
    stop_event: asyncio.Event,
) -> None:
    frames = ["⏳", "⌛", "⏳⌛", "⌛⏳"]
    idx = 0
    fact = get_today_history_fact()

    while not stop_event.is_set():
        try:
            await context.bot.edit_message_text(
                chat_id=chat_id,
                message_id=message_id,
                text=(
                    f"{frames[idx % len(frames)]} Анализирую имя {name}...\n\n"
                    f"{fact}"
                ),
            )
            await context.bot.send_chat_action(chat_id=chat_id, action=ChatAction.TYPING)
        except Exception:
            logger.exception("Failed to update waiting animation")
        idx += 1
        try:
            await asyncio.wait_for(stop_event.wait(), timeout=1.6)
        except asyncio.TimeoutError:
            pass


